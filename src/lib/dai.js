const ADDRESS     = '0x6B175474E89094C44Da98b954EedeAC495271d0F'
const BENEFICIARY = 'osarrouy.argent.xyz'

export const DAI = {
  userHasEnoughBalance: async (amount) => {
    try {
      const provider = new ethers.providers.Web3Provider(window.ethereum)
      const dai      = new ethers.Contract(ADDRESS, ABI, provider.getSigner())
      const balance  = new BN((await dai.balanceOf(window.ethereum.selectedAddress)).toString())
    
      if (balance.isLessThan(amount)) {
        return false
      } else {
        return true
      }
    } catch (e) {
      console.log(e.message)
      return false
    }
  },
  tip: async (amount) => {
    const provider = new ethers.providers.Web3Provider(window.ethereum)
    const dai      = new ethers.Contract(ADDRESS, ABI, provider.getSigner())
    const tx       = await dai.transfer(BENEFICIARY, amount)

    return tx
  }
}
